const grade = "C";

switch(grade) {
    case 'A':
        console.log("A");
        break;
    case 'B':
        console.log('B');
        break;
    case 'C':
        console.log("C");
        break;
    case 'D':
        console.log("D");
        break;
    default:
        console.log("No match");
}